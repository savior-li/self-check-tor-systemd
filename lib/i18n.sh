#===============================================================================
# 翻译函数
#===============================================================================
t() {
    local key="$1"
    local default="$2"
    local lang="${CURRENT_LANGUAGE:-en}"
    local translation=""
    
    case "${lang}" in
        zh) eval "translation=\${TRANSLATIONS_zh[$key]:-}" ;;
        es) eval "translation=\${TRANSLATIONS_es[$key]:-}" ;;
        *)  eval "translation=\${TRANSLATIONS_en[$key]:-}" ;;
    esac
    
    if [[ -n "${translation}" ]]; then
        echo "${translation}"
    else
        echo "${default}"
    fi
}

# 设置语言
set_language() {
    CURRENT_LANGUAGE="$1"
}

# 获取语言
get_language() {
    echo "${CURRENT_LANGUAGE:-en}"
}

# 获取语言名称
get_language_name() {
    case "$1" in
        zh) echo "中文" ;;
        es) echo "Español" ;;
        en) echo "English" ;;
        *)  echo "English" ;;
    esac
}